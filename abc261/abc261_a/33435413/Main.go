package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func solve(L []int, R []int) {
	w := bufio.NewWriter(os.Stdout)
	defer func() {
		if err := w.Flush(); err != nil {
			panic(err)
		}
	}()

	if L[0] > L[1] {
		L[0], L[1] = L[1], L[0]
		R[0], R[1] = R[1], R[0]
	}
	if L[1] >= R[0] {
		fmt.Fprintln(w, 0)
		return
	}
	if R[0] >= R[1] {
		fmt.Fprintln(w, R[1]-L[1])
		return
	}
	fmt.Fprintln(w, R[0]-L[1])
}

func main() {
	s := NewScanner()
	L := make([]int, 2)
	R := make([]int, 2)
	for i := 0; i < 2; i++ {
		L[i] = s.Int()
		R[i] = s.Int()
	}
	solve(L, R)
}

type Scanner struct {
	*bufio.Scanner
}

func NewScanner() *Scanner {
	s := bufio.NewScanner(os.Stdin)
	s.Buffer(make([]byte, 4096), 1048576)
	s.Split(bufio.ScanWords)
	return &Scanner{
		Scanner: s,
	}
}

func (s *Scanner) Int() int {
	if ok := s.Scan(); !ok {
		panic(s.Err())
	}
	v, err := strconv.Atoi(s.Text())
	if err != nil {
		panic(err)
	}
	return v
}

func (s *Scanner) IntN(n int) []int {
	v := make([]int, n)
	for i := 0; i < n; i++ {
		v[i] = s.Int()
	}
	return v
}

func (s *Scanner) IntN2(n int) ([]int, []int) {
	v1 := make([]int, n)
	v2 := make([]int, n)
	for i := 0; i < n; i++ {
		v1[i] = s.Int()
		v2[i] = s.Int()
	}
	return v1, v2
}

